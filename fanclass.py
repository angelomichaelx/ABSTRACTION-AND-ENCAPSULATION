# Create fan class
class Fan: 
            
#3 constants(Slow, Medium and Fast)
      SLOW = 1
      MEDIUM = 2
      FAST = 3

#constructor
      def __init__(self, speed = SLOW, on = False, radius = 5, color = "blue"):

            self.speed = speed
            self.on = 1 
            self.radius = radius
            self.color = color

#methods

      def get__speed(self):
            return self.speed
            
      def set_speed(self, speed):
            self.speed = speed

      def set_on(self):
            return self.on

      def set_on(self, on):
            self.on = on

      def set_radius(self):
            return self.radius

      def set_radius(self, radius):
            self.radius = radius
            
      def set_color(self):
            return self.color

      def set_color(self, color):
            self.color = color
            
    
    
    
    


          
